
//let é a definição de uma variável, ou seja, um espaço na memória do computador onde podemos armazenar um valor. O valor pode ser alterado durante a execução do programa.
let variavel = 10;

//const é a definição de uma constante, ou seja, um espaço na memória do computador onde podemos armazenar um valor. O valor não pode ser alterado durante a execução do programa.
const constante = 20;

console.log('Esse é meu primeiro programa em JavaScript!');

// Imprime o valor da variável e da constante no console, use a "," para separar os argumentos.
console.log('Valor da variável:', variavel);
console.log('Valor da constante:', constante);